#String Concatenation = Gluing together or combining multiple strings.
print("20 days are " + str(50) + " minutes")

#Below is a cleaner way of outputting the same data. F stands for Format.
print(f"20 days are {50} minutes")

#Below is how you would write the code if you wanted the correct answer.
print(f"20 days are {20 * 24 * 60} minutes")